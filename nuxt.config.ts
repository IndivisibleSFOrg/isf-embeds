import { execSync } from 'child_process';

const gitDescribe = (() => {
  try {
    return execSync('git describe --always --dirty=+').toString().trim();
  } catch {
    return 'dev';
  }
})();

const gitBranch = (() => {
  try {
    return execSync('git rev-parse --abbrev-ref HEAD').toString().trim();
  } catch {
    return 'local';
  }
})();

